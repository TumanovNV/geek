//Напишите программу, которая принимает на вход пятизначное число и проверяет, является ли оно палиндромом.


using System;

namespace PalindromeChecker
{
    class Program
    {
        static void Main(string[] args)
        {
            // Запросить у пользователя ввод пятизначного числа
            Console.Write("Введите пятизначное число: ");

            // Считать введенную строку
            string input = Console.ReadLine();

            // Проверить, что введенная строка состоит из 5 символов
            if (input.Length != 5)
            {
                // Если строка не состоит из 5 символов, вывести ошибку
                Console.WriteLine("Ошибка: введенное число должно состоять из 5 символов!");
            }
            else
            {
                // Если строка состоит из 5 символов, проверить, является ли она палиндромом

                // Изначально считаем, что число является палиндромом
                bool isPalindrome = true;

                // Пройти по первой половине введенной строки и сравнить ее с симметричной ей второй половиной
                for (int i = 0; i < input.Length / 2; i++)
                {
                    if (input[i] != input[input.Length - 1 - i])
                    {
                        // Если символы не совпадают, то число не является палиндромом
                        isPalindrome = false;
                        break;
                    }
                }

                // Вывести сообщение о том, является ли число палиндромом
                if (isPalindrome)
                {
                    Console.WriteLine("Введенное число является палиндромом!");
                }
                else
                {
                    Console.WriteLine("Введенное число не является палиндромом!");
                }
            }

            // Ожидать нажатия клавиши перед выходом из программы
            Console.ReadKey();
        }
    }
}
