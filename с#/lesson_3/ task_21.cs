// Напишите программу, которая принимает на вход координаты двух точек и находит расстояние между ними в 3D пространстве.

using System; // Подключаем пространство имен System

namespace DistanceCalculator // Создаем новый namespace (пространство имен) с именем DistanceCalculator
{
    class Program // Объявляем класс Program
    {
        static void Main(string[] args) // Объявляем метод Main, который будет выполняться при запуске программы
        {
            // Ввод координат первой точки
            Console.Write("Введите координаты первой точки через пробел: "); // Просим пользователя ввести координаты первой точки
            string[] point1 = Console.ReadLine().Split(' '); // Читаем введенную строку и разбиваем ее на подстроки по пробелу
            double x1 = double.Parse(point1[0]); // Преобразуем первую подстроку в число и присваиваем его переменной x1
            double y1 = double.Parse(point1[1]); // Преобразуем вторую подстроку в число и присваиваем его переменной y1
            double z1 = double.Parse(point1[2]); // Преобразуем третью подстроку в число и присваиваем его переменной z1

            // Ввод координат второй точки
            Console.Write("Введите координаты второй точки через пробел: "); // Просим пользователя ввести координаты второй точки
            string[] point2 = Console.ReadLine().Split(' '); // Читаем введенную строку и разбиваем ее на подстроки по пробелу
            double x2 = double.Parse(point2[0]); // Преобразуем первую подстроку в число и присваиваем его переменной x2
            double y2 = double.Parse(point2[1]); // Преобразуем вторую подстроку в число и присваиваем его переменной y2
            double z2 = double.Parse(point2[2]); // Преобразуем третью подстроку в число и присваиваем его переменной z2

            // Вычисление расстояния между точками
            double distance = Math.Sqrt(Math.Pow(x2-x1, 2) + Math.Pow(y2-y1, 2) + Math.Pow(z2-z1, 2)); // Вычисляем расстояние между двумя точками, используя формулу

            // Вывод результата
            Console.WriteLine($"Расстояние между точками: {distance:F2}"); // Выводим результат на экран с помощью Console.WriteLine(), используя интерполяцию строк для ограничения вывода до двух десятичных знаков.
        }
    }
}

