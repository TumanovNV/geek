// Задача 4: Напишите программу, которая принимает на вход три числа и выдаёт максимальное из этих чисел.

using System;

namespace MaxNumber // объявляем пространство имен
{
    class Program // объявляем класс Program
    {
        static void Main(string[] args) // объявляем метод Main
        {
            // Ввод трех чисел
            Console.WriteLine("Введите три числа:"); // выводим сообщение на консоль
            int num1 = int.Parse(Console.ReadLine()); // читаем первое число с консоли и преобразуем его в целое число
            int num2 = int.Parse(Console.ReadLine()); // читаем второе число с консоли и преобразуем его в целое число
            int num3 = int.Parse(Console.ReadLine()); // читаем третье число с консоли и преобразуем его в целое число

            // Нахождение максимального числа
            int maxNum = num1; // предполагаем, что первое число максимально
            if (num2 > maxNum) // если второе число больше текущего максимального
            {
                maxNum = num2; // обновляем максимальное число
            }
            if (num3 > maxNum) // если третье число больше текущего максимального
            {
                maxNum = num3; // обновляем максимальное число
            }

            // Вывод результата
            Console.WriteLine($"Максимальное число: {maxNum}"); // выводим результат на консоль

            Console.ReadKey(); // ожидаем нажатия клавиши, чтобы окно консоли не закрылось сразу после вывода результата
        }
    }
}
