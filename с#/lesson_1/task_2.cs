// Задача 2: Напишите программу, которая на вход принимает два числа и выдаёт, какое число большее, а какое меньшее.

using System;

class Program
{
    static void Main(string[] args)
    {
        // Выводим сообщение на экран и принимаем первое число от пользователя
        Console.Write("Введите первое число: ");
        int a = int.Parse(Console.ReadLine());

        // Выводим сообщение на экран и принимаем второе число от пользователя
        Console.Write("Введите второе число: ");
        int b = int.Parse(Console.ReadLine());

        // Определяем максимальное число
        int max = a > b ? a : b;

        // Определяем минимальное число
        int min = a < b ? a : b;

        // Выводим результат на экран
        Console.WriteLine($"Максимальное число: {max}");
        Console.WriteLine($"Минимальное число: {min}");

        // Ждем, пока пользователь не нажмет любую клавишу, чтобы закрыть консоль
        Console.ReadLine();
    }
}
